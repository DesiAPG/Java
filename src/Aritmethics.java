public class Aritmethics {

    /* Clase 2
       Aritmetica
     */

    public static void main(String[] args) {
        int number1, number2, result;
        number1 = 10;
        number2 = 20;

        // Addition
        result = number2 + number1;
        System.out.println(number2 + "+" + number1 + " is: "+result);

        // Substraction
        result = number2 - number1;
        System.out.println(number2 + "-" + number1 + " is: "+result);

        // Multiplication
        result = number2 * number1;
        System.out.println(number2 + "*" + number1 + " is: "+result);

        // Difference
        result = number2 / number1;
        System.out.println(number2 + "/" + number1 + " is: "+result);





    }
}
