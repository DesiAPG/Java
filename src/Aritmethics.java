public class Aritmethics {

    /*
     * Class 2
     * Aritmethics
     */

    public static void main(String[] args) {
        int number1, number2, result;
        number1 = 10;
        number2 = 20;

        // Addition
        result = number2 + number1;
        System.out.println(number2 + " + " + number1 + " = " + result);

        // Substraction
        result = number2 - number1;
        System.out.println(number2 + " - " + number1 + " = " + result);

        // Multiplication
        result = number2 * number1;
        System.out.println(number2 + " * " + number1 + " = " + result);

        // Difference
        result = number2 / number1;
        System.out.println(number2 + " / " + number1 + " = " + result);

    }
}
